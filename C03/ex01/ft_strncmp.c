int		ft_strncmp(char *s1, char *s2, unsigned int nb)
{
	unsigned int		i;

	i = 0;
	if (nb == 0)
		return (0);
	while (s1[i] == s2[i] && s1[i] && s2[i] && i < nb - 1)
	{
		i++;
	}
	return (s1[i] - s2[i]);
}
