#include <unistd.h>

void	ft_print_comb(void)
{
	char	a;
	char	b;
	char	c;
	int		d;

	d = 12;
	while (d < 789)
	{
		a = d / 100 + '0';
		b = d % 100 / 10 + '0';
		c = d % 10 + '0';
		if (a < b && b < c && d < 789)
		{
			write(1, &a, 1);
			write(1, &b, 1);
			write(1, &c, 1);
			write(1, ", ", 2);
			d++;
		}
		else
			d++;
	}
	write(1, "789", 3);
}
