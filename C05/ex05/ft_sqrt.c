int	ft_sqrt(int nb)
{
	int i;
	int x;

	i = 1;
	x = 1;
	if (nb <= 0)
		return (0);
	while (i < 20)
	{
		x = (x + (nb / x)) / 2;
		i++;
	}
	if (x * x == nb && x <= 46340)
		return (x);
	return (0);
}
